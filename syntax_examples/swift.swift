// Simple Swift Example
import Foundation

/**
 Greets the given name.
 Multi-line doc comment.
 - Parameter name: The name to greet.
 */
func greet(name: String) {
    // Print to console using string interpolation
    print("Hello, \(name)!")
}

let user = "Swift Developer" // Constant holding the name
greet(name: user) // Call the function
/* Another style of comment */